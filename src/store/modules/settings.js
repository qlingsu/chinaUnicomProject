import variables from '@/styles/element-variables.scss'

const showSettings = false;//Whether show the settings right-panel
const tagsView = true;//Whether need tagsView
const fixedHeader = false;//Whether fix the header
const sidebarLogo = false;//Whether show the logo in sidebar

const state = {
  theme: variables.theme,
  showSettings: showSettings,
  tagsView: tagsView,
  fixedHeader: fixedHeader,
  sidebarLogo: sidebarLogo
}

const mutations = {
  CHANGE_SETTING: (state, { key, value }) => {
    // eslint-disable-next-line no-prototype-builtins
    if (state.hasOwnProperty(key)) {
      state[key] = value
    }
  }
}

const actions = {
  changeSetting({ commit }, data) {
    commit('CHANGE_SETTING', data)
  }
}

export default {
  namespaced: true,
  state,
  mutations,
  actions
}

